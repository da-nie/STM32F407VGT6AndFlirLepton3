//----------------------------------------------------------------------------------------------------
inline void IL9325_SetWindow(uint16_t x_left,uint16_t y_top,uint16_t x_right,uint16_t y_bottom)
{
 //указываем окно	
 IL9325_WriteRegister16(ILI932X_HOR_START_AD,x_left);
 IL9325_WriteRegister16(ILI932X_HOR_END_AD,x_right);
 IL9325_WriteRegister16(ILI932X_VER_START_AD,y_top);
 IL9325_WriteRegister16(ILI932X_VER_END_AD,y_bottom);
 //указываем текущие координаты	
 IL9325_WriteRegister16(ILI932X_GRAM_HOR_AD,x_left);
 IL9325_WriteRegister16(ILI932X_GRAM_VER_AD,y_top);
 //выбираем регистр вывода изображения
 IL9325_SelectRegister(ILI932X_RW_GRAM);
}
