//----------------------------------------------------------------------------------------------------
//инициализация LCD-экрана
//----------------------------------------------------------------------------------------------------
void IL9325_Init(void)
{
 //инициализируем дисплей
 IL9325_Reset();
 IL9325_CS_Zero();
 
 uint32_t command_amount=sizeof(IL9325_InitList)/sizeof(uint16_t);
 for(uint32_t n=0;n<command_amount;)
 {
  uint16_t reg=IL9325_InitList[n];
  n++;
  uint16_t value=IL9325_InitList[n];
  n++;
  if (reg==DELAY_CODE)//пауза
  {
   HAL_Delay(value);  
   continue;
  }
  //значение для регистра
  IL9325_WriteRegister16(reg,value);  
 }
}
