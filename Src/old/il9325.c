//передать байт
//----------------------------------------------------------------------------------------------------
inline void IL9325_Write8(uint8_t byte)
{ 
 *((volatile uint8_t*)(LCD_DATA))=byte;
}
//----------------------------------------------------------------------------------------------------
//передать 16 бит 
//----------------------------------------------------------------------------------------------------
inline void IL9325_Write16(uint16_t value) 
{ 
 IL9325_Write8((value>>8)&0xFF);
 IL9325_Write8(value&0xFF);
}
//----------------------------------------------------------------------------------------------------
//выбрать регистр
//----------------------------------------------------------------------------------------------------
inline void IL9325_SelectRegister(uint16_t reg) 
{ 
 *((volatile uint8_t*)(LCD_REG))=(reg>>8)&0xFF;
 *((volatile uint8_t*)(LCD_REG))=reg&0xFF;
}
//----------------------------------------------------------------------------------------------------
//записать в регистр 16 бит
//----------------------------------------------------------------------------------------------------
inline void IL9325_WriteRegister16(uint16_t reg,uint16_t value) 
{ 
 IL9325_SelectRegister(reg);	
 IL9325_Write16(value);
}
